export function switc () {

    let dia =3;

    switch(dia){
    case 1:
        console.log ("domingo")
        break;
    case 2:
        console.log ("segunda")
        break;
    case 3:
        console.log ("terça")
        break;


    default:
        console.log ("dia invalido")
        break;
    }

    return (dia)
}

